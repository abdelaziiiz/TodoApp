    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        //return $request;
        //validation input data and send it to database finnally redirect to todos page
        $request->validate([
            'title' => 'required|unique:todos|min:8|max:25',
            'description' => 'required',
        ]);


        $new_todo = new Todo();
        $new_todo->title = $request->title;
        $new_todo->description = $request->description;
        $new_todo->save();
        return redirect('/todos');
    }

